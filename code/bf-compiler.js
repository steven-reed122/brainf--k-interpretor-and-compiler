// This is a simple brainfuck compiler that takes a string of
// commands in brainfuck and creates C++ code from it
// that will execute the brainfuck code

const fs = require('fs');

function emit(output, level, str) {
  while (level-- > 0) {
    output += "    ";
  }
  output += str;
  output += "\n";
  return output;
}

function bf_compiler(bf_code) {
  let output = '';
  let level = 1;
  prefix = `
#include <iostream>
#include <vector>
  
int const data_size = 10000;

void bf_execution()
{
    std::vector<char> data(data_size, 0);
    auto data_ptr = data.begin();
`;

  suffix = `
}

int main() {
  try
  {
    bf_execution();
    return 0;
  }
  catch (std::exception& e)
  {
    std::cerr << "exception: " << e.what() << '\\n';
    return 1;
  }
  catch (...)
  {
    std::cerr << "Some exception\\n";
    return 2;
  }
}`;

  output += prefix;

  for (const char of bf_code) {
    switch (char) {
      case '>':
        output = emit(output, level, "++data_ptr;");
        break;
      case '<':
        output = emit(output, level, "--data_ptr;");
        break;
      case '+':
        output = emit(output, level, "++*data_ptr;");
        break;
      case '-':
        output = emit(output, level, "--*data_ptr;");
        break;
      case '.':
        output = emit(output, level, "std::putchar(*data_ptr);");
        break;
      case ',':
        output = emit(output, level, "*data_ptr = std::getchar();");
        break;
      case '[':
        output = emit(output, level, "while (*data_ptr) {");
        level++;
        break;
      case ']':
        level--;
        output = emit(output, level, "}");
        break;
    }
  }

  output += suffix;

  console.log(output);
  return output;
}

